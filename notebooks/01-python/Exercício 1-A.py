#Exercício 1-A
n1 = int(input('Digite o primeiro número: '))
n2 = int(input('Digite o segundo número: '))
soma = n1 + n2
sub = n1 - n2
mult = n1 * n2
div = n1 / n2
print(f'O resultado da soma: {n1} + {n2} = {soma}')
print(f'O resultado da subtração: {n1} - {n2} = {sub}')
print(f'O resultado da multiplicação: {n1} x {n2} = {mult}')
print(f'O resultado da divisão: {n1} / {n2} = {div:.2f}')